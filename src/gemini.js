const { GoogleGenerativeAI } = require('@google/generative-ai');

const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({ model: 'gemini-3.5-flash' });

/**
 * วิเคราะห์ข้อความจากกลุ่ม LINE
 * คืนค่า JSON หรือ null ถ้าไม่ใช่การแจ้งปัญหา
 */
async function analyzeComplaint(text) {
  const prompt = `
คุณคือระบบวิเคราะห์การแจ้งปัญหาแมลงในอาคาร
วิเคราะห์ข้อความนี้แล้วตอบเป็น JSON array เท่านั้น ห้ามมีข้อความอื่น

ข้อความ: "${text}"

ข้อความอาจมีการแจ้งปัญหาหลายรายการในข้อความเดียว (เช่น "1. ... 2. ...") ให้แยกเป็นหลาย object ใน array

ถ้ามีการแจ้งปัญหาเกี่ยวกับแมลงหรือสัตว์รบกวน ให้ตอบแบบนี้:
[
  {
    "is_complaint": true,
    "pest_type": "ชนิดแมลงหรือสัตว์ (เช่น มดปีก, แมลงสาบ, ปลวก, แมลงวัน, หนู, แมงมุม)",
    "location": "อาคาร/ตึก/หน่วยที่พบ (เช่น ตึก 72 ปี, หน่วย NICU, ศูนย์เต้านม, ห้องอาหาร) ไม่รวมชั้น",
    "floor": "ชั้นที่พบ เช่น ชั้น 2, ชั้น 7 (ถ้าไม่ระบุใส่ ไม่ระบุ)",
    "severity": "น้อย หรือ ปานกลาง หรือ มาก",
    "contact_name": "ชื่อผู้ติดต่อในข้อความ เช่น คุณแข, คุณ TOR (ถ้าไม่มีใส่ ไม่ระบุ)",
    "contact_phone": "เบอร์โทรศัพท์ในข้อความ เช่น 024194620 (ถ้าไม่มีใส่ ไม่ระบุ)",
    "is_followup": false,
    "is_clarification": false,
    "summary": "สรุปปัญหาสั้นๆ ภาษาไทย"
  }
]

is_clarification — ตั้งเป็น true เมื่อข้อความเป็น "การชี้แจง/รายงานผลสำรวจ/แนะนำวิธีแก้" ที่ทีมงาน AGA ตอบกลับลูกค้า (ไม่ใช่การแจ้งปัญหาใหม่):
- ข้อความที่อ้างอิงเรื่องเดิม เช่น "จากที่มีข้อร้องเรียนเรื่อง...", "จากการเข้าสำรวจ...", "จากการตรวจสอบ..."
- มีการแนะนำหรือรายงานผล เช่น "ผมแนะนำให้ติดตั้ง...", "ได้ดำเนินการ...", "สาเหตุเนื่องจาก..."
- เป็นการอธิบายสาเหตุ/วิธีการแก้ไข ไม่ใช่การรายงานพบเห็นปัญหาใหม่
- ข้อความมีลักษณะเป็น "รายงาน" หรือ "คำแนะนำ" จากทีมงาน ไม่ใช่ "ขอความช่วยเหลือ" จากผู้แจ้ง
- ถ้าเป็นการแจ้งพบปัญหาใหม่จริงๆ → is_clarification: false
- หมายเหตุ: is_clarification ยังเป็น is_complaint: true (สกัด pest/location/floor ตามปกติ) แค่ติดธง is_clarification: true

is_followup — ตั้งเป็น true เมื่อรายการนั้นเป็น "การแจ้งซ้ำ / ตามงานเดิม" (ไม่ใช่ปัญหาใหม่):
- มีคำบ่งบอกการแจ้งซ้ำ เช่น "รอบ2", "รอบที่ 2", "แจ้งครั้งที่ 2", "แจ้งซ้ำ", "โทรแจ้งรอบ2", "ตามงาน", "งานเดิม", "งานตัวเดียวกับ Wxxx"
- บอกว่าเคยแจ้งแล้วแต่ยังไม่ได้ทำ เช่น "แจ้งแล้วเมื่อวันที่... ยังไม่ได้ดำเนินการ", "ยังไม่มีคนมาทำ"
- ถ้าเป็นการแจ้งครั้งแรกปกติ → is_followup: false
- หมายเหตุ: การแจ้งซ้ำยังเป็น is_complaint: true (สกัด pest/location/floor ตามปกติ) แค่ติดธง is_followup: true

ถ้าข้อความนี้ไม่เกี่ยวกับการแจ้งปัญหาแมลงเลย ให้ตอบ:
[{ "is_complaint": false }]

ชื่ออาคารมาตรฐาน — ให้แปลงชื่อที่รับมาให้เป็นชื่อมาตรฐานเสมอ ก่อนใส่ใน location:
สยามมินทร์ / สยามินทร์ / ตึกสยามินทร์ → ตึกสยามินทร์
ตึก100ปีพระศีร / ตึก 100 ปี สมเด็จพระศรีฯ / ตึก100 ปีสมเด็จพระศรี / ตึก100ปีสมเด็จ → อาคาร 100 ปี สมเด็จพระศรีนครินทร์
เฉลิมพระเกียรติ / อาคารเฉลิมพระเกียรติ → อาคารเฉลิมพระเกียรติ
ตึกเจ้าฟ้ามหาจักรี / เจ้าฟ้ามหาจักรี → ตึกเจ้าฟ้ามหาจักรี
อำนวยการ / ตึกอำนวยการ → ตึกอำนวยการ
หอพักพยาบาล 3 / หอพักพยาบาล3 → หอพักพยาบาล 3

กฎ:
- ตอบเป็น JSON array เสมอ แม้มีแค่ 1 รายการ
- ถ้าข้อความมีหลายรายการ (มีหมายเลข 1. 2. หรือแยกย่อหน้า) ให้แยกเป็นหลาย object
- ถ้าไม่ระบุชนิดแมลงชัดเจน ให้เดาจากบริบท
- ถ้าไม่ระบุสถานที่ ให้ใส่ "ไม่ระบุ"
- severity: น้อย=เห็นนิดหน่อย, ปานกลาง=พอมี, มาก=เยอะมาก/รุนแรง
- สกัดชื่อผู้ติดต่อและเบอร์โทรแยกตามแต่ละรายการ
- "หนู" ในภาษาไทยมี 2 ความหมาย: (1) สรรพนาม "ฉัน/ผม" เช่น "หนูได้ค่ะ", "หนูจะไป", "หนูทำ" → ไม่ใช่การแจ้งปัญหา (2) สัตว์รบกวน → ต้องมีบริบทชัดเจน เช่น "พบหนู", "เก็บหนู", "มีหนูในห้อง" จึงจะถือว่าเป็นการแจ้งปัญหา
- ข้อความที่เป็นการตอบรับ นัดหมาย หรือสนทนาทั่วไป เช่น "ได้ค่ะ", "รับทราบ", "โอเค", "วันที่..." → ไม่ใช่การแจ้งปัญหา

ข้อความต่อไปนี้ ไม่ใช่การแจ้งปัญหา แม้จะมีการพูดถึงแมลงหรือสัตว์ก็ตาม → ตอบ [{ "is_complaint": false }] เสมอ:
- คำสั่งปิดงาน / ปิด Work Order: ข้อความที่มีคำว่า "ปิดงาน" หรือ typo เช่น "ปอดงาน" ไม่ว่าจะอยู่ตำแหน่งไหน เช่น "ปิดงาน W009 กาวดักหนู 1ตัว", "W009 ปิดงาน ดักหนูได้ 1ตัว", "ปิดงาน W015", "ปอดงาน W028 แจ้งงานซ้ำ" — เป็นคำสั่งปิด Work Order ไม่ใช่การแจ้งปัญหาใหม่
- สรุปการประชุม / รายงานการประชุม / บันทึกการประชุม: ขึ้นต้นด้วย "สรุปการประชุม", "สรุปประชุม", "รายงานการประชุม" หรือมีเนื้อหาเป็นการสรุปสิ่งที่คุยในที่ประชุม
- ขอข้อมูลหรือสถิติ: "รบกวนขอข้อมูล", "ขอข้อมูลสถิติ", "ข้อมูลเปรียบเทียบ", "รบกวนส่งข้อมูล"
- กล่าวถึงปัญหาในเชิงภาพรวม/ประวัติ: "พบบ่อยในพื้นที่...", "มีปัญหาในทุกอาคาร" ที่อยู่ในบริบทการรายงานหรือประชุม ไม่ใช่การพบเห็นใหม่ ณ ขณะนั้น
- แผนงานหรือนัดหมายล่วงหน้า: "นัดหมายการเข้าพื้นที่", "วางแผนตรวจ", "กำหนดการ"
- การประสานงานหรือติดตามงาน: "ประสานลูกค้า", "ติดตามผล", "รับทราบ"
`;

  try {
    const result = await model.generateContent(prompt);
    const raw = result.response.text().trim();
    const cleaned = raw.replace(/```json\n?/g, '').replace(/```\n?/g, '').trim();
    const parsed = JSON.parse(cleaned);
    return Array.isArray(parsed) ? parsed : [parsed];
  } catch (err) {
    console.error('Gemini error:', err.message);
    return null;
  }
}

module.exports = { analyzeComplaint };
